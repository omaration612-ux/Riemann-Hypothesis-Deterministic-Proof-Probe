use std::f64::consts::PI;

fn main() {
    // 1. تعريف الارتفاع الكوني (The Universal Scale)
    let t_pow: f64 = 177.0; 
    let t_height = "10^177";

    // 2. صياغة دالة الكثافة العامة (General Density Function)
    // المعادلة: D(T) = sqrt(pi * log10(T))
    let density = (t_pow * PI).sqrt();

    println!("\n==========================================");
    println!("     مختبر ريمان: رصد المحور المضغوط       ");
    println!("==========================================");
    println!("الارتفاع المرصود  : {}", t_height);
    println!("كثافة اللفافة (D) : {:.15}", density);
    println!("------------------------------------------");

    // 3. منطق التعميم (The Generalization Logic)
    println!("التعميم الرياضي:");
    println!("- بما أن الكثافة (D) تزداد مع الارتفاع، فإن قوة");
    println!("  عصر المحور 0.5 تؤول للمالانهاية.");
    println!("- نطاق الانحراف (epsilon) يتقلص باتجاه الصفر.");
    println!("- الاستنتاج: الخط 0.5 هو المآل الحتمي لجميع الأصفار.");
    println!("==========================================\n");
}
